using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _04.for循环
{
    internal class Program
    {
        static void Main(string[] args)
        {
            for (int i = 0; i < 10; i++)
            {
                Console.WriteLine("hello world");
            }

            for (int i = 10; i > 0; i--)
            {
                Console.WriteLine("hello world");
            }


            Console.ReadLine();
        }
    }
}
